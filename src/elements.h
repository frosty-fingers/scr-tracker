// CGB background palettes, one per element, applied via BKGF_CGB_PAL0-3
// tile attributes. 4 colors each: background/fill/fill/outline slots at
// indices 0/2/3 (index 1 unused - reserved as a lighter accent if ever
// needed). Ignored entirely on DMG hardware (see init_graphics()).
#define PAL_AIR    0u
#define PAL_EARTH  1u
#define PAL_FIRE   2u
